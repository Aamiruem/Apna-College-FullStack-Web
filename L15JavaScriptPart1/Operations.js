let a = 10
let b = 20

//addition
let sum = a + b
console.log(sum)

//subtraction
let sub = a - b
console.log(sub)

//multiplication
let mul = a * b
console.log(mul)

//division
let div = a / b
console.log(div)

//remainder
let mod = a % b
console.log(mod)

//exponent or power
let exp = a ** b
console.log(exp)

// ++x => x = x + 1 pre increment
let x = 5;
console.log(++x); // 6
console.log(x);   // 6

// --y => y = y - 1 pre decrement
let y = 5;
console.log(--y); // 4
console.log(y);   // 4

// z++ => z = z + 1 post increment
let z = 5;
console.log(z++); // 5
console.log(z);   // 6

// k-- => k = k - 1 post decrement
let k = 5;
console.log(k--); // 5
console.log(k);   // 4
