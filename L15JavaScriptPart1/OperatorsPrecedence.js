// operator precedence

// ()> **> *> /> %
let x = (2+1) * 3
console.log(x);
// **> %
let y = 8 ** 2 / 4
console.log(y);

// +/-
let z = 1 + 2 - 3
console.log(z);


// * / %
let a = 1 * 2 / 3
console.log(a);

// + -
let b = 1 + 2 - 3 * 4
console.log(b);

// ==!=
let c = 1 == 1
console.log(c);

let d = 1 == 2
console.log(d);

let e = 1 != 2
console.log(e);

let f = 1 != 1
console.log(f);

// < > <= >=
let g = 1 < 2
console.log(g);



