// Assignment Operators
// =, +=, -=, *=, /=, %=, **=, <<=, >>=, >>>=, |=, &=, ^= 
//x = x + 5
let x = 10;
x += 5
console.log(x);

//x = x - 5
let y = 10;
y -= 5
console.log(y);

//x = x * 5
let z = 10;
z *= 5
console.log(z);

//x = x / 5
let a = 10;
a /= 5
console.log(a);

//x = x % 5
let b = 10;
b %= 5
console.log(b);

//x = x ** 5
let c = 10;
c **= 5
console.log(c);


//x = x << 5
let d = 10;
d <<= 5
console.log(d);


//x = x >> 5
let e = 10;
e >>= 5
console.log(e);


//x = x >>> 5
let f = 10;
f >>>= 5
console.log(f);


//x = x | 5
let g = 10;
g |= 5
console.log(g);


//x = x & 5
let h = 10;
h &= 5
console.log(h);


//x = x ^ 5
let i = 10;
i ^= 5
console.log(i);


//x = x ** 5
let j = 10;
j **= 5
console.log(j);



