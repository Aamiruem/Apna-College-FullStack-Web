// print odd numbers (1 to 15)

for(let i = 1; i<= 15; i = i+2){
    console.log(i);
}

console.log("\n odd backward");

//backward loop
for(let i = 15; i>=1; i = i-2){
    console.log(i);
}

console.log("\n");


