// Returns the first index of occurrence of some value in string or gives  -1 if not found.

// IndexOf() => It means searching the strings for a specific character.

let str = "i love coding";

console.log(str.indexOf("love"));       // 2
console.log(str.indexOf("j"));          // -1
console.log(str.indexOf("o"));          // 3
console.log(str.indexOf("love", 5));    // -1
console.log(str.indexOf("o", 5));       // 7












