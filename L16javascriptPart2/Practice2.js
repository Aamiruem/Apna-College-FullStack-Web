let size = "L";

if (size == "XL") {
    console.log("price of XL size popcorn = RS.250 ");
} else if (size == "L") {
    console.log("price of L size popcorn = RS.200 ");
} else if (size == "M") {
    console.log("price of M size popcorn = RS.150 ");
} else if (size == "s") {
    console.log("price of S size popcorn = RS.100 ");
} else {
    console.log("Invalid size");
}
